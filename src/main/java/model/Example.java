package model;

public class Example {
    private String englishPart;
    private String vietnamesePart;

    public void setEnglishPart(String englishPart) {
        this.englishPart = englishPart;
    }

    public void setVietnamesePart(String vietnamesePart) {
        this.vietnamesePart = vietnamesePart;
    }

    public String getVietnamesePart() {
        return this.vietnamesePart;
    }

    public String getEnglishPart() {
        return this.englishPart;
    }
}